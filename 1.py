# Declaring an empty list
list1 = []
#  create a list of Numbers
list1 = [90, 95, 92, 97, 99]
print(list1)

# checking Type
print(type(list1))

# length of a list
print(len(list1))

# a list of strings
list2 = ["Hi", "Hello", "Hola"]
print(list2)
print(len(list2))

# Index of an element
print(list1.index(95))

# Accessing list element using Positive Index
print(list1[0])

# Accessing list element using Negative Index
print(list1[-1])

print(list2[-1])

# Accessing list element using Slice Operator
print(list1[0:5])

print(list2[0:5])

print(list1[0:3])

print(list2[0:3])

print(list1[-5:-3])

print(list2[-4:-2])

